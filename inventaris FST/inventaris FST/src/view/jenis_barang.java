    private void dataTabel() {
        tampilData.setVisible(false);
        tambahData.setVisible(true);
        
        int row = tbl_data.getSelectedRow();
        judul2.setText("Perbarui Data Jenis");
        
        tf_kode.setEnabled(false);
        
        tf_kode.setText(tbl_data.getModel().getValueAt(row, 1).toString());
        tf_nama.setText(tbl_data.getModel().getValueAt(row, 2).toString());
        
        aktif();
        btn_tambah1.setText("PERBARUI");
        btn_clear.setText("BATAL");
        btn_batal1.setVisible(false);
        btn_batal.setVisible(true);
    }
    
    private void aktif() {
        tf_nama.setEnabled(true);
    }

    private void hapusData() {
        int index = tbl_data.getSelectedRow();
        if(index!=-1){
            Model_JenisBarang brg = tblMod.getData(tbl_data.convertRowIndexToModel(index));
            if(JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
                servis.hapusData(brg);
                tblMod.hapusData(index);
                loadData();
                resetForm();  
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan dihapus");
        }
    }
    
     private boolean validasiInput() {
     boolean valid = false;
     if(tf_kode.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Kode Jenis tidak boleh kosong");
    }else if (tf_nama.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(null, "Nama Jenis tidak boleh kosong");
    }else{
        valid = true;
    }
     return valid;
    }
     
    private void simpanData() {
        if(validasiInput()==true){
            String kode = tf_kode.getText();
            String nama = tf_nama.getText();
            
            Model_JenisBarang jb = new Model_JenisBarang();
            
            jb.setKode_jenis(kode);
            jb.setNama_jenis(nama);
              
            servis.tambahData(jb);
            tblMod.tambahData(jb);
            loadData();
            resetForm();
            tampilPanel();
            btn_tambah1.setText("TAMBAH");
        }
    }

    private void perbaruiData() {
        int index = tbl_data.getSelectedRow();
        if (index != -1) {
            Model_JenisBarang mod = tblMod.getData(tbl_data.convertRowIndexToModel(index));

            if (validasiInput() == true) {
                String kode = tf_kode.getText();
                String nama = tf_nama.getText();

                Model_JenisBarang jb = new Model_JenisBarang();

                jb.setKode_jenis(kode);
                jb.setNama_jenis(nama);

                servis.perbaruiData(jb);
                tblMod.perbaruiData(index, mod);
                loadData();
                resetForm();
                tampilPanel();

            }
        }
    }
    
    private void pencarian() {
       List<Model_JenisBarang> list = servis.pencarian(tf_pencarian.getText().toLowerCase());
        tblMod.setData(list);
    }
  
