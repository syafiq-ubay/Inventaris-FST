    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        String x = btn_tambah.getText();
        int row = tbl_data.getSelectedRow();
        tf_kode.setEnabled(false);
        if (x.equals("TAMBAH")) {
            
            mainPanel.removeAll();
            mainPanel.repaint();
            mainPanel.revalidate();

            mainPanel.add(tambahData);
            mainPanel.repaint();
            mainPanel.revalidate();
            
            judul2.setText("Tambah Data Jenis");
            btn_tambah1.setText("SIMPAN");
            tf_kode.setEnabled(false);
            tf_kode.setText(servis.nomor());
            loadData();
        } else {
            int index = tbl_data.getSelectedRow();
            if (index != -1) {
                Model_JenisBarang mod = tblMod.getData(tbl_data.convertRowIndexToModel(index));

                if (x.equals("UBAH")) {
                    dataTabel();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pilih dahulu data yang akan diubah");
            }
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

