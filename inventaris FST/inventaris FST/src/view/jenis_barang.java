    private void btn_eksporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eksporActionPerformed
        JnaFileChooser fileChooser = new JnaFileChooser();
        Window window = null;
        boolean action = fileChooser.showSaveDialog(window);

        if (action) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath() + ".xlsx";
            TableMod_JenisBarang model = (TableMod_JenisBarang) tbl_data.getModel();
            List<Model_JenisBarang> exportedData = servis.getData();
            servis.exportToExcel(exportedData, filePath);
        }
        loadData();
    }//GEN-LAST:event_btn_eksporActionPerformed

    private void btn_imporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imporActionPerformed
        JnaFileChooser fileChooser = new JnaFileChooser();
        Window window = null;
        boolean action = fileChooser.showOpenDialog(window);

        if (action) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            List<Model_JenisBarang> importedData = new ArrayList<>();
            servis.importFromExcel(importedData, filePath);
            tblMod.setData(importedData);
        }
        loadData();
    }//GEN-LAST:event_btn_imporActionPerformed


