    private void hapusData() {
        int index = tbl_data.getSelectedRow();
        if (index != -1) {
            Model_BarangKeluar bk = tblMod.getData(tbl_data.convertRowIndexToModel(index));
            if (JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                servis.hapusData(bk);
                tblMod.hapusData(index);
                loadData();
                resetForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan dihapus");
        }
    }

    private boolean validasiInput() {
        boolean valid = false;
        if (t_kodeBK.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode barang keluar tidak boleh kosong");
        } else if (t_tgl.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tanggal pemesan tidak boleh kosong");
        } else if (t_idPemesan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID Pemesan tidak boleh kosong");
        } else {
            valid = true;
        }
        return valid;
    }

    private void simpanData() {
        if (validasiInput()) {
            String kode = t_kodeBK.getText();
            String idPem = t_idPemesan.getText();
            String unit = t_unit.getText();
            String idAdmin = lb_ID.getText();
            String tgl = t_tgl.getText();

            Model_BarangKeluar bk = new Model_BarangKeluar();
            Model_Pemesan p = new Model_Pemesan();
            Model_Admin a = new Model_Admin();

            bk.setKode_keluar(kode);
            p.setId_pemesan(idPem);
            p.setUnit_pemesan(unit);
            a.setId_admin(idAdmin);

            try {
                // Parse the string date to java.util.Date
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd"); // Replace "your_date_format" with the actual date format
                Date tglDate = sdf.parse(tgl);
                bk.setTgl_keluar(tglDate);
            } catch (ParseException e) {
                e.printStackTrace(); // Handle the parsing exception appropriately
                return; // Return or handle error accordingly
            }

            bk.setPemesanbarang(p);
            bk.setAdmin(a);

            servis.tambahData(bk);
            tblMod.tambahData(bk);
        }
    }

