    private void btn_hapussemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapussemActionPerformed
        int index = tbl_sementara.getSelectedRow();
        if (index != -1) {
            if (JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                DefaultTableModel tb = (DefaultTableModel) tbl_sementara.getModel();
                tb.removeRow(tbl_sementara.getSelectedRow());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan dihapus");
        }
    }//GEN-LAST:event_btn_hapussemActionPerformed

    private void btn_tambahSemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahSemActionPerformed

        if (validasiInput() == true) {
            if (evt.getSource() == btn_tambahSem) {
                String kodeOnderdil = t_kodeBarang.getText();
                int jmh = Integer.parseInt(t_jumlah.getText());

                if (cekStok(kodeOnderdil, jmh)) {
                    DefaultTableModel tb = (DefaultTableModel) tbl_sementara.getModel();
                    tb.addRow(new Object[]{
                        t_kodeBarang.getText(),
                        t_namaBarang.getText(),
                        t_jumlah.getText(),});

                    JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan!");
                } else {
                    JOptionPane.showMessageDialog(this, "Stok Barang melebihi stok yang tersedia!", "Peringatan", JOptionPane.WARNING_MESSAGE);

                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan!");
        }
    }//GEN-LAST:event_btn_tambahSemActionPerformed

